// ───────────────────────────────────────────────────────────────────────────
// СПИСЪК С РЕАЛНИТЕ ПРОДУКТИ (whitelist)
//
// В Sanity има повече продуктови записи, отколкото реални артикули с истинска
// информация. Тук държим slug-овете на 24-те ПОТВЪРДЕНИ реални продукта.
// Всичко извън този списък се показва в магазина като празна „coming soon"
// карта (без снимки, без информация, без линк) и винаги се подрежда най-отзад.
//
// ───────────────────────────────────────────────────────────────────────────
// ⚙️  КАК ДА ВЪРНЕМ/ДОБАВИМ ПРОДУКТ (на по-късен етап):
//   • Когато попълним нов реален артикул в Sanity → добави неговия slug тук.
//     Веднага става реална, кликваема карта и заема място пред празните.
//   • За да върнем ВСИЧКИ стари продукти наведнъж (старо поведение) →
//     сложи COMING_SOON_ENABLED = false. Тогава целият whitelist се игнорира
//     и всеки продукт от Sanity отново е реална карта.
// ───────────────────────────────────────────────────────────────────────────

// Главен ключ. false → изключва изцяло логиката за празни карти (всичко реално).
export const COMING_SOON_ENABLED = true;

// Slug-овете на реалните 24 продукта (редът тук няма значение — подредбата в
// магазина се пази от полето `order` в Sanity).
