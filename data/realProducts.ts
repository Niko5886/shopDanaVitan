// Реален ли е даден продукт (има ли истинска информация и трябва ли да е
// кликваем). При COMING_SOON_ENABLED = false всичко се води за реално.
export function isRealProduct(slug: string): boolean {
  if (!COMING_SOON_ENABLED) return true;
  return REAL_PRODUCT_SLUGS.has(slug);
}
